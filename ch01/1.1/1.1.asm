;产生一个周期为 1ms 的方波，并在示波器上显示
;clk0 接 2MHZ

DSEG SEGMENT
	T0  EQU 280H
	T1  EQU 281H
	T2  EQU 282H
	CTL EQU 283H
DSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG
START:
	MOV AX,DSEG
	MOV DS,AX

	MOV DX,CTL
	MOV AL,00110111B
	OUT DX,AL
	
	MOV DX,T0
	MOV AL,00H
	OUT DX,AL
	MOV AL,20H
	OUT DX,AL

	MOV AH,4CH
	INT 21H
CSEG ENDS
END START