;产生周期为10ms，负脉宽100us的连续脉冲
;clk0 接按键开关

DSEG SEGMENT
	T0 EQU 280H
	T1 EQU 281H
	T2 EQU 282H
	CLT EQU 283H
DSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG
START:
	MOV AX,DSEG
	MOV DS,AX

	MOV DX,CTL
	MOV AL,00100101B
	OUT DX,AL
	
	MOV DX,T0
	MOV AL,02H
	MOV DX,AL

	MOV DX,CTL
	MOV AL,00100101B
	OUT DX,AL
	
	MOV DX,T1
	MOV AL,01H
	MOV DX,AL

	MOV AH,4CH
	INT 21H
CSEG ENDS
END START