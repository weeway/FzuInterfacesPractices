	ASSUME CS:CSEG, DS:DSEG
START:
	MOV AX,DSEG
	MOV DS,AX

	MOV DX,CTL
	MOV AL,00100101B
	OUT DX,AL
	
	MOV DX,T0
	MOV AL,02H
	MOV DX,AL

	MOV DX,CTL
	MOV AL,01100101B
	OUT DX,AL
	
	MOV DX,T1
	MOV AL,01H
	MOV DX,AL

	MOV AH,4CH
	INT 21H
CSEG ENDS
END START