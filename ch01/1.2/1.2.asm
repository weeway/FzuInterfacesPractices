	MOV AX,DSEG
	MOV DS,AX

	MOV DX,CLT
	MOV AL,00110101B ;方式2
	OUT DX,AL

	MOV DX,T0
	MOV AL,00H
	OUT DX,AL

	MOV AL,20H
	OUT DX,AL
	
	MOV AH,4CH
	INT 21H
CSEG ENDS
END  START