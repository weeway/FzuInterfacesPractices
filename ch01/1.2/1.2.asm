;2.产生周期为 1ms 的连续负脉冲，并在示波器上显示
;计数值 2000
DSEG SEGMENT
	T0  EQU 280H
	T1  EQU 281H
	T2  EQU 282H
	CLT EQU 283H
DSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG

START:
	MOV AX,DSEG
	MOV DS,AX

	MOV DX,CLT
	MOV AL,00110101B ;先低字节，后高字节，方式2，BCD码
	OUT DX,AL

	MOV DX,T0
	MOV AL,00H
	OUT DX,AL

	MOV AL,20H
	OUT DX,AL
	
	MOV AH,4CH
	INT 21H
CSEG ENDS
END  START