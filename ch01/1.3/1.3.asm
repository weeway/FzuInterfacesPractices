;改程序接受按键产生的信号进行计数，10次后结束，并使LED亮
;clk0 接按键开关

DSEG SEGMENT
	T0 EQU 280H
	T1 EQU 281H
	T2 EQU 283H
	T3 EQU 284H
DSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG
START:
	MOV AX,DSEG
	MOV DS,AX

	MOV DX,CTL
	MOV AL,00010001B
	OUT DX,AL
	
	MOV DX,T0
	MOV AL,09H
	MOV DX,AL

	MOV AH,4CH
	INT 21H
CSEG ENDS
END START