;6、产生周期为 10ms ,负脉冲宽度为 1 ms 的连续脉冲。 
DSEG SEGMENT
	T0  EQU 280H
	T1  EQU 281H
	T2  EQU 282H
	CLT EQU 283H
DSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG

START:
	MOV AX,DSEG
	MOV DS,AX

	;T0 | 产生周期为 1ms 的负脉冲
	MOV DX,CLT
	MOV AL,00110101B ;先低字节，后高字节，方式2，BCD码
	OUT DX,AL

	MOV DX,T0
	MOV AL,00H
	OUT DX,AL

	MOV AL,20H
	OUT DX,AL

	
	;T1 | 以 T0 的 OUT 为 CLK, 产生周期为 10ms 负脉冲
	MOV DX,CLT
	MOV AL,01010101B 
	OUT DX,AL

	MOV DX,T1
	MOV AL,10H
	OUT DX,AL	

	MOV AH,4CH
	INT 21H
CSEG ENDS
END  START
	