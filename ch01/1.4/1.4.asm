;4.产生 1S 方波，并在 LED 上显示
;计数值 T0=2000、T1=1000
DSEG SEGMENT
	T0  EQU 280H
	T1  EQU 281H
	T2  EQU 282H
	CLT EQU 283H
DSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG

START:
	MOV AX,DSEG
	MOV DS,AX


	MOV DX,CLT
	MOV AL,00110111B ;方式3；计数值 BCD码 表示；先写低4位，后写高4位
	OUT DX,AL

	MOV DX,T0
	MOV AL,00H
	OUT DX,AL

	MOV AL,20H
	OUT DX,AL

	
	MOV DX,CLT
	MOV AL,01110111B 
	OUT DX,AL

	MOV DX,T1
	MOV AL,00H
	OUT DX,AL

	MOV AL,10H
	OUT DX,AL	

	MOV AH,4CH
	INT 21H
CSEG ENDS
END  START