
;4.产生 1S 方波，并在 LED 上显示
;计数值 2000、1000
DSEG SEGMENT
	T0  EQU 280H
	T1  EQU 281H
	T2  EQU 282H
	CLT EQU 283H
DSEG ENDS

CSEG SEGMENT
	ASSUME CS:CSEG, DS:DSEG

START:
	MOV AX,DSEG
	MOV DS,AX

	;T1 | 产生 1ms 的方波
	MOV DX,CLT
	MOV AL,00110111B ;先低字节，后高字节，方式3，BCD码
	OUT DX,AL

	MOV DX,T0
	MOV AL,00H
	OUT DX,AL

	MOV AL,20H
	OUT DX,AL

	
	;T1 | 以 T0 的 OUT 为 CLK, 产生 1s 方波
	MOV DX,CLT
	MOV AL,01110111B 
	OUT DX,AL

	MOV DX,T1
	MOV AL,00H
	OUT DX,AL

	MOV AL,10H
	OUT DX,AL	

	MOV AH,4CH
	INT 21H
CSEG ENDS
END  START